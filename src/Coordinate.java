/**
 * The Coordinate class serves as a template for (x,y) coordinates.
 * This class is used by the Main, Car, and Grid classes
 *
 * @author  Jacob Byerine
 * @version 1.0
 * @since   2021-09-08
 */
public class Coordinate {
    public int x;
    public int y;
    public Coordinate(){}
    public Coordinate(int x, int y){
        this.x = x;
        this.y = y;
    }
}
